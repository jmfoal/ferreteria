    private void botonCrearFerreteriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonCrearFerreteriaActionPerformed
        CrearFerreteria dialogo = new CrearFerreteria(this,true);
        dialogo.setVisible(true);
        if(dialogo.isAceptado()){
            ferreteria = new Ferreteria(dialogo.getRazon(), dialogo.getDireccion(), dialogo.getTitular());
            this.botonCrearFerreteria.setEnabled(false);
            this.botonCargarFerreteria.setEnabled(false);
            this.jPanel1.setEnabled(true);
            this.jButton5.setEnabled(true);
        }
    }//GEN-LAST:event_botonCrearFerreteriaActionPerformed

    private void botonCargarFerreteriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonCargarFerreteriaActionPerformed
        CargarFerreteria dialogo = new CargarFerreteria(this, true);
        dialogo.setVisible(true);
        if(dialogo.isAceptado()){
            try {
                ferreteria = new Ferreteria(dialogo.getRazon());
                this.botonCrearFerreteria.setEnabled(false);
                this.botonCargarFerreteria.setEnabled(false);
                this.jPanel1.setEnabled(true);
                this.jButton5.setEnabled(true);
                
                this.mostrarTabla();
                this.jTable1.setModel(tabla);
               
            } catch (IOException ex) {                
            }
        }
    }//GEN-LAST:event_botonCargarFerreteriaActionPerformed

    
