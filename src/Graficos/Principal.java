    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        AñadirHerramienta dialogo = new AñadirHerramienta(this, true);
        
        dialogo.setVisible(true);
        
        if(dialogo.isAceptado()){
            ferreteria.añadirHerramienta(new Herramienta(dialogo.getNombre(), dialogo.getMarca(), dialogo.getModelo(), dialogo.getTamaño(), dialogo.getPrecio(), dialogo.getExistencias()));
            this.mostrarTabla();
            this.jTable1.setModel(tabla);
            
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String marca;
        String modelo;
        if(this.jTable1.getSelectedRow() != -1){
            
            marca = tabla.getValueAt(this.jTable1.getSelectedRow(), 1).toString();
            modelo = tabla.getValueAt(this.jTable1.getSelectedRow(), 2).toString();
            
            try{
                ferreteria.eliminarHerramienta(marca, modelo);
            }
            catch(HerramientaInexistente e){
                JOptionPane.showMessageDialog(this, "La herramienta no existe");
            }
            this.mostrarTabla();
            jTable1.setModel(tabla);
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

